                    <h2>Vrijwilligers gezocht</h2>
                    <h5>
                        Hospice Amsterdam Zuidoost zal, na opening, voornamelijk afhankelijk zijn van vrijwilligers. Naast het team van coördinatoren die de dagelijkse gang van zaken in het hospice runnen, en de professionele zorgverleners, zullen veel werkzaamheden uitgevoerd worden door vrijwilligers.
                        De vrijwilligers voor het Hospice Amsterdam Zuidoost zullen zorg dragen voor het waken bij bewoners, hulp bieden bij lichte lichamelijke verzorging, emotionele steun bieden aan bewoners of naasten en de mantelzorgers bijstaan. Deze werkzaamheden zullen uit diensten van max. 4 aaneengesloten uren bestaan, tussen 7 uur ‘s ochtends en 23 uur ’s avonds.
                        Het werk in het hospice kan zwaar zijn voor de vrijwilligers, omdat het vrijwilligerswerk zich afspeelt in de sfeer van sterven en dood. 
                    </h5>
                    <h5>
                        Daarnaast kan het werk ook verrijkend zijn, omdat de vrijwilliger bijdraagt aan een mooi einde aan het leven van een bewoner van het hospice. Tijdens het vrijwilligerswerk komt de vrijwilliger in contact met verschillende mensen en wordt er kennis vergaard van verschillende culturen.
                        Hospice Amsterdam Zuidoost is daarom op zoek naar een groep van 75 - 100 vrijwilligers die ondersteuning willen bieden na het openen van de deuren van het hospice. Vrijwilligers zijn verzekerd door het hospice tijdens hun werk, krijgen scholing met betrekking tot de werkzaamheden, eventuele onkosten worden vergoed, er wordt zorg gedragen voor het geestelijk en lichamelijk welbevinden, daarnaast worden vervolgcursussen aan vrijwilligers aangeboden die geruime periode naar behoren functioneren.
                        Heeft u interesse, vul onderstaand formulier in (door op de knop te drukken) en mail dit naar: info@hospiceamsterdamzuidoost.nl.<br> Wilt u meer informatie belt u (06) 442 70 584.
